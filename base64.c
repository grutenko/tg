int base64_decode(struct base64_ctx *ctx, uint8_t *b, size_t len, uint8_t *out)
{
        uint8_t *p = b, *op = out;
        while (len)
        {
                if ((ctx->seen_eq && *p != 0x3d) || b64dec[*p] == 0xFF)
                        return -1;
                
                if (*p == 0x3D)
                {
                        if (ctx->seen_eq >= 2 || ctx->bc < 2)
                                return -1;

                        ctx->seen_eq += 1;
                }

                ctx->b[ctx->bc++] = b64dec[*p++];
                len--;

                if (ctx->bc < 4)
                        continue;

                *op++ = (ctx->b[0] << 2) | (ctx->b[1] >> 6);
                if (ctx->b[2] != 0x80)
                        *op++ = (ctx->b[1] << 4) | (ctx->b[2] >> 2);
                if (ctx->b[3] != 0x80)
                        *op++ = (ctx->b[2] << 6) | ctx->b[3];

                ctx->bc = 0;
        }

        return op - out;
}