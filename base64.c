void base64_init(struct base64_ctx *ctx)
{
        ctx->b[0] = 0;
        ctx->b[1] = 0;
        ctx->b[2] = 0;
        ctx->b[3] = 0;
        ctx->bc = 0;
        ctx->seen_eq = 0;
}

